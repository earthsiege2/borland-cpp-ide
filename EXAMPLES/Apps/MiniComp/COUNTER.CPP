#include "counter.h"
//-------------------------------------------------------------------------
TCounter::TCounter(int Multiple)
{
  FMultiple = Multiple;
}
//-------------------------------------------------------------------------
void TCounter::Clear()
{
  FVal = 0;
}
//-------------------------------------------------------------------------
void TCounter::Increment()
{
  if (((++FVal) % FMultiple) == 0)
      OnMultiple(this);
}
